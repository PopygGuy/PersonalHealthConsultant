  static String? getAdviceForNorm(String normName, int score) {
    if (score >= 5) return null; // Excellent result implies no urgent advice needed
    
    final normKey = _normalizeNormName(normName);
    
    // Check specific advice for the score
    if (_adviceMap.containsKey(normKey)) {
      final scoreAdvice = _adviceMap[normKey]?[score];
      if (scoreAdvice != null) return scoreAdvice;
    }

    // Fallback if exact norm name not found but category matches
    if (normKey.contains("бег") && normKey.contains("м")) return _getRunningAdvice(score, normKey);
    if (normKey.contains("подтягивани")) return _getPullupAdvice(score);
    if (normKey.contains("отжимани")) return _getPushupAdvice(score);
    if (normKey.contains("наклон") || normKey.contains("гибкость")) return _getFlexibilityAdvice(score);
    if (normKey.contains("прыжок")) return _getJumpAdvice(score);
    if (normKey.contains("пресс") || normKey.contains("туловищ")) return _getAbsAdvice(score);
    if (normKey.contains("плавани")) return _getSwimmingAdvice(score);
    if (normKey.contains("лыж")) return _getSkiingAdvice(score);
    if (normKey.contains("стрельб")) return _getShootingAdvice(score);
    if (normKey.contains("метан")) return _getThrowingAdvice(score);

    return "Регулярные тренировки помогут улучшить этот результат. Обратитесь к преподавателю за индивидуальным планом.";
  }

  static String _normalizeNormName(String name) {
    return name.toLowerCase().trim();
  }

